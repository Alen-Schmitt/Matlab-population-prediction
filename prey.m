% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
syms growth_rate_prey death_rate_prey growth_rate_predator death_rate_predator time ...
    population_prey population_predator
growth_rate_prey=str2num(get(handles.edit9, 'string'));
death_rate_prey=str2num(get(handles.edit10, 'string'));
growth_rate_predator=str2num(get(handles.edit11, 'string'));
death_rate_predator=str2num(get(handles.edit12, 'string'));
 population_prey0=10;
 population_predator0=10;
 time=0:0.0001:100.0;
 delta_time=time(2)-time(1);
 ntime=length(time);
 population_prey=zeros(size(time));
 population_predator=zeros(size(time));
 population_prey(1)=population_prey0;
 population_predator(1)=population_predator0;
 for j=1:(ntime-1)
    population_prey(j+1)=population_prey(j)+delta_time*(population_prey(j)*(growth_rate_prey-death_rate_prey*population_predator(j)));  
    population_predator(j+1)=population_predator(j)+delta_time*(population_predator(j)*(growth_rate_predator*population_prey(j)-death_rate_predator));  
 end

%%plot polulations according to time
hax=handles.axes1;
plot(hax, time, population_prey,'b-', time, population_predator, 'g--')
axis(hax, [0,100,0,30]);
grid on;
xlabel(hax, 'time')
ylabel(hax, 'population')
